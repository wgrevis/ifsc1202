n = int(input("Enter Fibonnaci Sequence Number: "))
while n != 0 :
  if n == 0:
    count = 0
  if n == 1:
    count = 1
  if n == 2:
    count = 1
  if n == 3:
    count = 2
  if n == 4:
    count = 3
  if n == 5:
    count = 5
  if n == 6:
    count = 8
  if n == 7:
    count = 13
  if n == 8:
    count = 21
  if n == 9:
    count = 34
  if n == 10:
    count = 55
  print("Fibonacci Number : ", count)  